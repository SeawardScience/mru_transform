#include "mru_transform/sensor.h"

namespace mru_transform
{

std::string getROSType(std::string topic)
{
  ros::master::V_TopicInfo topic_info;
  ros::master::getTopics(topic_info);

  for(const auto t: topic_info)
    if(t.name == topic)
      return t.datatype;
  return "";
}


} // namespace mru_transform
